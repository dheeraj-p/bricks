class Paddle {
  constructor(height, width, bottom, left) {
    this.height = height;
    this.width = width;
    this.bottom = bottom;
    this.left = left;
  }

  getHeight() {
    return this.height;
  }

  getWidth() {
    return this.width;
  }

  getBottom() {
    return this.bottom;
  }

  getLeft() {
    return this.left;
  }
}
